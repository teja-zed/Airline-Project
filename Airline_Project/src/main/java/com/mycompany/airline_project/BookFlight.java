    private void searchFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFlightBtnActionPerformed
        // TODO add your handling code here:
        
        // previous data populated into the 'flightsTable' should be cleared
        DefaultTableModel model = (DefaultTableModel) flightsTable.getModel();
        model.setRowCount(0);
        
        // whenever the table is repopulated
        // clear the fare related fields
        fareField.setText("");
        totalTickets.setText("");
        totalFareField.setText("");
        
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "user", "1234");
            
            // get origin and destination
            String origin_db = originComboBox.getSelectedItem().toString();
            String destination_db = destinationComboBox.getSelectedItem().toString();
            globalOrigin = origin_db;
            globalDestination = destination_db;
            //
            
            pre = con.prepareStatement("SELECT id, name, date, duration, fare FROM flights WHERE origin=? AND destination=?");
            pre.setString(1, origin_db);
            pre.setString(2, destination_db);
            
            ResultSet res = pre.executeQuery();
            // get metadata
            ResultSetMetaData rsmd = res.getMetaData();
            
            int cols = rsmd.getColumnCount();
            // now create a default table format model which will be of the above format
            DefaultTableModel defTableModel = (DefaultTableModel) flightsTable.getModel();
            // keep reading rows into res using res.next()
            while ( res.next() ) {
                // Vector instead of ArrayList because we don't need to provide any datatype
                Vector vec = new Vector();
                for (int i=0; i<cols; i++) {
                    vec.add(res.getString("id"));
                    vec.add(res.getString("name"));
                    vec.add(res.getString("date"));
                    vec.add(res.getString("duration"));
                    vec.add(res.getString("fare"));
                }
                defTableModel.addRow(vec);
            }
            if ( defTableModel.getRowCount() == 0 )
                JOptionPane.showMessageDialog(this, "Sorry.\n" + "No flights found "
                                                                + "from '" + origin_db + "' to '" + destination_db + "'");
            //
            
            //
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchFlightBtnActionPerformed

