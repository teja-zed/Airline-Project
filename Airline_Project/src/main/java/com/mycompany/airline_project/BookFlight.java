    private void calculateFareBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateFareBtnActionPerformed
        // TODO add your handling code here:
        
        String fareStr = fareField.getText();
        String totalTicketsStr = totalTickets.getText();
        
        String probMsgStr = "";
        if ( fareStr.equals("") ) {
            probMsgStr += "Select a flight ";
        }
        if ( totalTicketsStr.equals("") ) {
            if (probMsgStr.length() != 0)
                probMsgStr += "and ";
            probMsgStr += "Enter No. of Tickets ";
        }
        if ( probMsgStr.length() != 0 ) {
            probMsgStr += "to calculate Total Fare";
            JOptionPane.showMessageDialog(this, probMsgStr);
            return;
        }
        
        int priceTkt = Integer.parseInt(fareStr);
        int numTkt = Integer.parseInt(totalTicketsStr);
        
        int totalPrice = priceTkt * numTkt;
        totalFareField.setText(String.valueOf(totalPrice));
    }//GEN-LAST:event_calculateFareBtnActionPerformed

