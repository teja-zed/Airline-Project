    public void autoID() {
        try {
            Connection con;
            PreparedStatement pre;
            // Dependencies
            // Add Dependency
            // search query
            // "mysql"
            // select result
            // "be.yildiz-games : module-database-mysql > central/local"
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "user", "1234");
            //
            pre = con.prepareStatement("SELECT MAX(id) FROM tickets");
            ResultSet res = pre.executeQuery();
            
            res.next(); // init res
            if ( res.getString("MAX(id)") == null ) {
                ticketIDLabel.setText("TK001");
            }
            else {
                long id = Long.parseLong(res.getString("MAX(id)").substring(2));
                id++;
                ticketIDLabel.setText("TK" + String.format("%03d", id));
            }
            //
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

