    public void autoID() {
        try {
            Connection con;
            PreparedStatement pre;
            // Dependencies
            // Add Dependency
            // search query
            // "mysql"
            // select result
            // "be.yildiz-games : module-database-mysql > central/local"
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "user", "1234");
            //
            pre = con.prepareStatement("SELECT MAX(id) FROM customers");
            ResultSet res = pre.executeQuery();
            
            res.next(); // init res
            if ( res.getString("MAX(id)") == null ) {
                customerID.setText("CS001");
            }
            else {
                long id = Long.parseLong(res.getString("MAX(id)").substring(2));
                id++;
                customerID.setText("CS" + String.format("%03d", id));
            }
            //
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

