    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/Airline_Project", "user", "1234");
            
            // get all the fields needed for 'customers' table
            String customerID_db = customerID.getText();
            String firstName_db = firstName.getText();
            String lastName_db = lastName.getText();
            
            DateFormat dateFmt = new SimpleDateFormat("yyyy-MM-dd");
            String dateOfBirth_db = dateFmt.format(dateOfBirth.getDate());
            
            String gender_db = (maleGender.isSelected())? "Male":"Female";
            
            String passportNo_db = passportNo.getText();
            String nationalNo_db = nationalNo.getText();
            String addressTextArea_db = addressTextArea.getText();
            String contactNo_db = contactNo.getText();
            //
            
            pre = con.prepareStatement("INSERT INTO customers("
                                                            + "id, first_name, last_name"
                                                            + ", dob, gender"
                                                            + ", passport_no, national_no"
                                                            + ", address, contact_no"
                                                            + ")"
                                                            + "values(?, ?, ?, ?, ?, ?, ?, ?, ?)");
            pre.setString(1, customerID_db);
            pre.setString(2, firstName_db);
            pre.setString(3, lastName_db);
            pre.setString(4, dateOfBirth_db);
            pre.setString(5, gender_db);
            pre.setString(6, passportNo_db);
            pre.setString(7, nationalNo_db);
            pre.setString(8, addressTextArea_db);
            pre.setString(9, contactNo_db);
            
            pre.executeUpdate();
            
            // show that data is successfully inserted into the table
//            JOptionPane.showMessageDialog(null, "Customer added successfully", "Result", JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(this, "Customer added successfully");
            
            // this.setVisible(false);
            this.dispose();
            
            //
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

